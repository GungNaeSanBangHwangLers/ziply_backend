package ziply.review.service;

import org.springframework.stereotype.Component;

@Component
public class DirectionMapper {

    public record DirectionInfo(String type, String features, String pros, String cons) {
    }

    public DirectionInfo getInfo(Double degree) {
        if (degree == null) {
            return new DirectionInfo("미측정", "-", "-", "-");
        }

        double d = degree % 360;
        if (d < 0) {
            d += 360;
        }

        // 1. 북향 (337.5° ~ 22.5°)
        if (d >= 337.5 || d < 22.5) {
            return new DirectionInfo(
                    "북향",
                    "일조량 변화가 적고 하루 종일 밝기가 일정해요. 집중을 요하는 활동을 하는 분께 추천드려요.",
                    "여름에 시원해요. 빛이 적어 집중력 유지에 유리해요.",
                    "겨울에 추워서 난방비 부담이 커요. 해가 잘 안 들어 습할 수 있어요."
            );
        }
        // 2. 북동향 (22.5° ~ 67.5°)
        if (d >= 22.5 && d < 67.5) {
            return new DirectionInfo(
                    "북동향",
                    "이른 아침에만 해가 잠깐 들어와요. 어두운 환경에서 작업해야하는 분께 추천드려요.",
                    "집중을 요하거나, 햇빛에 예민한 물건이 많은 분에게 적합해요.",
                    "일조량이 부족해 어둡고 추울 수 있어요."
            );
        }
        // 3. 동향 (67.5° ~ 112.5°)
        if (d >= 67.5 && d < 112.5) {
            return new DirectionInfo(
                    "동향",
                    "아침 일찍 해가 뜨고 오후에는 서늘해요. 아침형 루틴을 가진 분께 추천드려요.",
                    "아침 햇살이 잘 들어와요. 여름 오후, 해가 덜 들어와 시원해요.",
                    "오후부터 어두워지고 겨울에 추울 수 있어요."
            );
        }
        // 4. 남동향 (112.5° ~ 157.5°)
        if (d >= 112.5 && d < 157.5) {
            return new DirectionInfo(
                    "남동향",
                    "오전 일찍 해가 뜨고 오후 일찍 해가 져요. 아침형 루틴을 가진 분께 추천드려요.",
                    "여름 오후, 해가 집 안으로 들어오는 시간이 적어 상대적으로 시원해요.",
                    "정남향 대비 겨울철 일조량이 부족할 수 있어요."
            );
        }
        // 5. 남향 (157.5° ~ 202.5°)
        if (d >= 157.5 && d < 202.5) {
            return new DirectionInfo(
                    "남향",
                    "하루 종일 햇빛이 잘 들어와요. 집에 머무는 시간이 많고 안정적인 일조량을 원하는 분께 추천드려요.",
                    "겨울에는 따뜻하고 여름에는 시원해서 냉난방비를 줄일 수 있어요.",
                    "보통 월세 가격이 비싸고, 매물이 적을 수 있어요. 햇빛 때문에 가구 변색이 있을 수 있어요."
            );
        }
        // 6. 남서향 (202.5° ~ 247.5°)
        if (d >= 202.5 && d < 247.5) {
            return new DirectionInfo(
                    "남서향",
                    "오전에 해가 비교적 늦게 뜨고 오후 시간대에 해가 더 길게 집 안에 머물러요. 오후 활동이 많은 분께 추천드려요.",
                    "오후 늦은 시간까지 햇볕이 들어와 겨울에 따뜻해요.",
                    "오후 늦은 시간까지 햇볕이 들어와 여름철 오후에 더워요."
            );
        }
        // 7. 서향 (247.5° ~ 292.5°)
        if (d >= 247.5 && d < 292.5) {
            return new DirectionInfo(
                    "서향",
                    "오후 내내 햇빛이 강하게 들어와요. 오후에 주로 활동하는 분께 추천드려요.",
                    "겨울에 따뜻해요. 늦은 오후까지 밝아요.",
                    "여름에 더워 냉방비가 비쌀 수 있어요. 오후 햇살이 눈부실 수 있어요."
            );
        }
        // 8. 북서향 (292.5° ~ 337.5°)
        if (d >= 292.5 && d < 337.5) {
            return new DirectionInfo(
                    "북서향",
                    "늦은 오후부터 해가 들어오고 비교적 일조량 적어요. 어두운 환경에서 작업해야하는 분께 추천드려요.",
                    "오후 늦게까지 자연 채광을 활용 가능해요.",
                    "일조량이 비교적 적고, 겨울철에 추워요."
            );
        }

        return new DirectionInfo("알 수 없음", "-", "-", "-");
    }
}